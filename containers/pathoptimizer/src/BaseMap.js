  /**
   * Create a new Map
   * 
   * @param {*} w 
   * @param {*} h 
   * @returns 
   */
  createNewMap(w = '', h = '') {
    var arr = new Array();

    var width = 10;
    var height = 10;

    if (w != '' && h != '') {
      width = w;
      height = h;
    }

    for (var y = 0; y < height; y++) {
      var arr2 = [];
      for (var x = 0; x < width; x++) {
        arr2.push(0);
      }
      arr.push(arr2);
    }
    this.map = arr;
    return arr;
  }

  /**
   * Get the empty fields of the map.
   * 
   * @returns {Array} Array of empty fields
   */
  getEmptyFields() {
    var arr = new Array();

    for (var y = 0; y < this.getHeight(); y++) {
      for (var x = 0; x < this.getWidth(); x++) {
        if (this.map[y][x] == 0) {
          arr.push(new Point2D(x, y));
        }
      }
    }
    return arr;
  }

