  /**
   * Check if a field is != 0
   * 
   * @param {*} x 
   * @param {*} y 
   * @returns boolean
   */
  isBlocked(x, y) {
    if (this.map[y][x] != 0) {
      return true;
    } else {
      return false;
    }
  }

  /**
   * Get the neighbours of a point
   * 
   * @param {*} point 
   * @returns 
   */
  getNeighboursArr(point) {
    var x = point.getX();
    var y = point.getY();
    var arr = [];
    if (x < this.getWidth() - 1) { if (!this.isBlocked(x + 1, y)) { arr.push(new Point2D(x + 1, y)); } }
    if (x > 0) { if (!this.isBlocked(x - 1, y)) { arr.push(new Point2D(x - 1, y)); } }
    if (y < this.getHeight() - 1) { if (!this.isBlocked(x, y + 1)) { arr.push(new Point2D(x, y + 1)); } }
    if (y > 0) { if (!this.isBlocked(x, y - 1)) { arr.push(new Point2D(x, y - 1)); } }

    return arr;
  }

  /**
   * Get a free random value on the map (=0) randomly
   * 
   * @returns 
   */
  getRandomFreeValueOn2DMap() {
    var emptyArr = this.getEmptyFields();
    var rand = Math.floor(Math.random() * emptyArr.length);
    var val = emptyArr[rand];
    emptyArr.splice(rand, 1);
    return val;
  }

  /**
   * Get the path as a 2D map
   * 
   * @param {*} path 
   * @returns 
   */
  getPathAs2DMap(path) {
    var finalWay = this.getEmptyMap();
    for (var j = 0; j < path.length; j++) {
      var y = path[j].coord.getY();
      var x = path[j].coord.getX();
      finalWay[y][x] = 1;
    }
    return finalWay;
  }

